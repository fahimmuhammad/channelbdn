$log = [];

function run($conn, $sql, &$log) {
    if ($conn->query($sql)) { $log[] = ['ok', mb_substr($sql, 0, 80)]; }
    else { $log[] = ['err', $conn->error . ' — ' . mb_substr($sql, 0, 80)]; }
}

// ── Tables ─────────────────────────────────────────────────────────────────
run($conn, "CREATE TABLE IF NOT EXISTS divisions (
  id TINYINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(60) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4", $log);

run($conn, "CREATE TABLE IF NOT EXISTS districts (
  id SMALLINT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  division_id TINYINT UNSIGNED NOT NULL,
